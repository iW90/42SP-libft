#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	while (n--)
		*d++ = *s++;
	return (dst);
}

/*
Copia `n` bytes de `src` para `dest`.
Retorna um ponteiro para `dest`.
*/